#include "3-calc.h"
#include <stdlib.h>
#include <stdio.h>
/**
 * op_add - returns the sum
 * @a: input one
 * @b: input two
 * Return: 0
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - the difference
 * @a: input one
 * @b: input two
 * Return: 0
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - returns the product
 * @a: input one
 * @b: input two
 * Return: 0
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - returns the division
 * @a: input one
 * @b: input two
 * Return: 0
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - returns the remainder
 * @a: input one
 * @b: input two
 * Return: 0
 */
int op_mod(int a, int b)
{
	return (a % b);
}

